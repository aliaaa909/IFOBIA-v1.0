    private void loadMataKuliah() {
        comboModel.removeAllElements();
        List<mataKuliah> list = dao.getAllMataKuliah();
        for (mataKuliah m : list) {
            comboModel.addElement(m);
        }
    }
    
  private void loadTabelPengumuman() {
        // Kolom 0 (ID) kita sembunyikan nanti
        tableModel.setColumnIdentifiers(new Object[]{"ID", "Matkul", "Judul", "Isi"});
        tableModel.setRowCount(0); 
        
        user pj = FormMenuUtama.getLoggedInUser();
        if (pj == null) return; // Safety check
        
        // PANGGIL METHOD FILTER (Bukan getPengumumanLengkap)
        List<pengumuman> list = dao.getPengumumanByPj(pj.getId_user()); 
        
        for (pengumuman p : list) {
            tableModel.addRow(new Object[]{
                p.getId_pengumuman(), // ID Asli dari database
                p.getNamaMk(),
                p.getJudul(),
                p.getIsi()
            });
        }
        
        // Sembunyikan Kolom ID (Index 0)
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getColumnModel().getColumn(0).setWidth(0);
    }

