    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          try {
            mataKuliah mk = (mataKuliah) jComboBox1.getSelectedItem();
            String judul = jTextField1.getText();
            String isi = jTextField2.getText();
            user pj = FormMenuUtama.getLoggedInUser(); 

            if (mk == null || judul.isEmpty() || isi.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
                return;
            }
            if (pj == null) {
                JOptionPane.showMessageDialog(this, "Sesi habis, login lagi.");
                return;
            }

            pengumuman p = new pengumuman(mk.getKode_mk(), judul, isi, pj.getId_user());
            
            if (dao.tambahPengumuman(p, pj.getId_user())) {
                JOptionPane.showMessageDialog(this, "SUKSES! Pengumuman tersimpan.");
                
                // Bersihkan form
                jTextField1.setText("");
                jTextField2.setText("");
                jComboBox1.setSelectedIndex(0);
                
                // UPDATE TABEL (PENTING!)
                loadTabelPengumuman(); 
                
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan.");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
int viewRow = jTable1.getSelectedRow();
        if (viewRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih dulu baris yang mau dihapus!");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Yakin hapus?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                // Konversi baris Tampilan -> baris Model (Penting kalau tabel disortir)
                int modelRow = jTable1.convertRowIndexToModel(viewRow);
                
                // Ambil ID dari kolom 0 di Model
                Object idObj = tableModel.getValueAt(modelRow, 0);
                int idPengumuman = Integer.parseInt(idObj.toString());
                
                // Cek ID di konsol output (buat debugging)
                System.out.println("Mencoba menghapus ID: " + idPengumuman);
                
                if (dao.hapusPengumuman(idPengumuman)) {
                    JOptionPane.showMessageDialog(this, "Berhasil dihapus!");
                    loadTabelPengumuman(); // Refresh tabel biar barisnya hilang
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus di database.");
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

