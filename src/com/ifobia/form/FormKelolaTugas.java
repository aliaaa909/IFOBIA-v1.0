    /**
     * Method 1: Memuat mata kuliah ke ComboBox
     */
    private void loadDataMataKuliah() {
        matkulComboBoxModel.removeAllElements(); // Kosongkan dulu
        List<mataKuliah> daftarMatkul = dao.getAllMataKuliah();
        for (mataKuliah matkul : daftarMatkul) {
            matkulComboBoxModel.addElement(matkul);
        }
    }
    
    /**
     * Method 2: Memuat data tugas (VERSI ADMIN)
     */
    private void loadDataTugasAdmin() {
        // Atur nama kolom (Hapus "Status")
        tableModel.setColumnIdentifiers(new Object[]{
            "ID", "Mata Kuliah", "Judul", "Deskripsi", "Deadline"
        });
        
        tableModel.setRowCount(0); 
        List<tugas> daftarTugas = dao.getAllTugasForAdmin();
        
        for (tugas t : daftarTugas) {
            tableModel.addRow(new Object[]{
                t.getId_tugas(),
                t.getNama_mk(),
                t.getJudul(),
                t.getDeskripsi(),
                t.getDeadline()
                // Tidak ada t.getIsSelesai()
            });
        }
        
        // Sembunyikan kolom ID (Opsional, tapi rapi)
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getColumnModel().getColumn(0).setWidth(0);
    }
    
