    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
            // 1. Ambil data dari form
            mataKuliah matkul = (mataKuliah) jComboBoxMatkul.getSelectedItem();
            if (matkul == null) {
                JOptionPane.showMessageDialog(this, "Pilih mata kuliah dulu!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            String kodeMk = matkul.getKode_mk();
            String judul = txtJudul.getText();
            String deskripsi = txtDeskripsi.getText();
            String deadline = txtDeadline.getText(); // "YYYY-MM-DD"
            
            if (judul.isEmpty() || deadline.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Judul dan Deadline tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 2. Buat objek 'tugas' baru (pakai konstruktor 1)
            tugas t = new tugas(kodeMk, judul, deskripsi, deadline);
            
            // 3. Simpan ke database
            boolean sukses = dao.tambahTugas(t);
            
            // 4. Update UI
            if (sukses) {
                JOptionPane.showMessageDialog(this, "Tugas berhasil ditambahkan dan didistribusikan ke mahasiswa!");
                loadDataTugasAdmin(); // Refresh tabel ADMIN
                clearForm();     // Kosongkan form
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambah tugas.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    
    // 1. Cek apakah ada baris yang dipilih
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris tugas yang ingin dihapus.");
        return;
    }
    
    // 2. Ambil ID tugas dari kolom tersembunyi (Kolom 0)
    int modelRow = jTable1.convertRowIndexToModel(selectedRow);
    int id_tugas = (int) tableModel.getValueAt(modelRow, 0); // Asumsi kolom 0 adalah ID
    String judul_tugas = tableModel.getValueAt(modelRow, 2).toString(); // Asumsi kolom 2 adalah Judul
    
    // 3. Konfirmasi penghapusan
    int confirm = JOptionPane.showConfirmDialog(this, 
            "Yakin ingin menghapus tugas:\n" + judul_tugas + "?", 
            "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            
    if (confirm == JOptionPane.YES_OPTION) {
        
        // 4. Panggil DAO untuk menghapus
        tugasDAO dao = new tugasDAO(); 
        if (dao.hapusTugas(id_tugas)) {
            JOptionPane.showMessageDialog(this, "Tugas berhasil dihapus!");
            
            // 5. Refresh tabel
            loadDataTugasAdmin(); 
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menghapus tugas.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    dao.getAllTugasForAdmin();
    }//GEN-LAST:event_jButton2ActionPerformed


